                    <p>According to documents from the mid-century XVI , <strong>Machu Picchu</strong> would have been one of the residences rest of <strong>Pachacutec</strong>, the ninth <strong>Inca Tahuantinsuyo</strong> between 1438 and 1470 . However, some of its finest buildings and ceremonial evident from the main path to <strong>the llaqta</strong> realize its earlier rise to <strong>Pachacutec</strong> and its presumable use as a <strong>religious sanctuary</strong>. Both uses, <strong>Palace and sanctuary</strong>, would not have been incompatible. Even though his supposed military discussed, so the popular epithets of "strength" or "citadel" could have been bettered.</p>
                    
                    <p><strong>Machu Picchu</strong> is considered both a masterpiece of architecture and engineering. Its unique architectural and landscape features, and the veil of mystery that has woven around much of the literature published on the site, have become one of the most popular tourist destinations on the planet.</p>
                    
                    <p>Machu Picchu is on the List of World Heritage of Unesco since 1983, as part of a cultural and ecological group known as the <strong>Historic Sanctuary of Machu Picchu</strong>. On July 7, 2007 <strong><a href="../machu-picchu-full-day.php">Machu Picchu</a></strong> was declared one of the new seven wonders of the modern world in a ceremony in Lisbon (Portugal), which was attended by one hundred million voters in the world.</p> 
