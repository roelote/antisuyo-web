                </div>
                <div class="col-md-10 descripcion">
                    <p>La plaza mayor del Cusco, fue secada durante la época del imperio incaico, iniciada por el inca Sinchi Roca, y terminada de secar por el inca Pachacútec, quien utilizó arena traída desde el litoral costeño. Hablar del Cusco, y especialmente de su Plaza de Armas, es hablar de un destino lleno de historia. Ya que inicialmente antes de los incas esto fue un pantano, y ahora con el transcurrir de los años , se ha convertido en unos de los ejes turísticos de la ciudad imperial del Cusco.</p> 
                   <p> Esta plaza llegó a convertirse en el núcleo administrativo, religioso y cultural de la capital del que fue uno de los más grandes imperios de América y del Mundo, estamos hablando de la "Sociedad Incaica"; luego con la llegada de los españoles, la Plaza de Armas fue rodeada de diversas construcciones como mansiones y templos, las cuales fueron levantadas sobre los palacios incas que existían en el contorno.</p>
