                    <p><strong><a href="../cusco-clasico-moderno">Sacsayhuamán</a></strong> es una de las edificaciones más asombrosas de todo el mundo, se piensa que empezó a construirse durante el gobierno de Pachacútec y que continuó por otros 70 años hasta Túpac Yupanqui; es el trabajo de más de 20,000 hombres que extrajeron las piedras desde las canteras de Muina, Huacoto y Rumicolca y las trasladaron 20 km hasta la colina de la ciudad del Cusco, donde las transformaron en la muralla que se puede ver ahora; y sin saberlo, en uno de los lugares más misteriosos del planeta. Las piedras de la muralla están tan bien encajadas, que entre ellas no cabe un alfiler; ¿no le parece asombroso? bueno entonces sepa que están unidas “por nada”; ningún tipo de amalgama, pegamento o cualquier tipo de argamasa las mantiene unidas.</p><p>
