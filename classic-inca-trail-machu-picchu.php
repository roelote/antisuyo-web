                <div class="col-md-10 descripcion"> 
					   
					 <p>The Inca Trail to Machu Picchu (also known as Camino Inca or Camino Inka) consists of three overlapping trails: Mollepata, Classic, and One Day. Mollepata is the longest of the three routes with the highest mountain pass and intersects with the Classic route before crossing Warmiwañusqa ("dead woman"). Located in the Andes mountain range, the trail passes through several types of Andean environments including cloud forest and alpine tundra. Settlements, tunnels, and many Incan ruins are located along the trail before ending the terminus at the Sun Gate on Machu Picchu Mountain. The two longer routes require an ascent to beyond 4,200 metres (13,800 ft) above sea level, which can result in altitude sickness.</p>

<p>The Inca Trail stretches from central Ecuador to central Chile in the south and the Pacific Ocean to the eastern slopes of the Andes.
The final destination is the Inca Trail Machu Picchu, the mysterious "Lost City of the Incas".</p>

<p>The Inca Trail is the most famous trek in South America and is rated by many as one of the top 5 ways of the world. A unique experience and every effort made to make this famous Inca Trail, it's worth doing!</p>

<p>The Inca Trail is a set of ancient stone paved road crossing over the mountains, the Urubamba River and came to the city of Machu Picchu in Peru.</p>

<p>The Inca Trail has a total of 45 kilometers, it manages to combine the beautiful landscape of mountains, lush forests and the subtropical forest. Of course, an impressive mix of stone buildings, archaeological sites, ruins and Inca tunnels.</p>

<p>Concerns about excessive use of the Inca Trail has led the Peruvian government put limitations on admission of people to the Inca Trail, so all visitors to the Inca Trail must apply the reservation in a travel agency and tourism with enough time advance</p>

<p>The number of people entering the Inca Trail is daily maximum of 500 people, of which only 200 are hikers and the rest are tour guides and porters, for that same month in June, July and August tend to sell spaces, therefore it is advisable to book the Inca Trail, a minimum 6-7 months in advance.</p>

<p>The Inca Trail in February is always closed for maintenance and cleaning of the entire Inca Trail route.</p>                 
