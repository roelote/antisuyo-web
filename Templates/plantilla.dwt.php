<p><strong>Machu  Picchu</strong> em espanhol significa &quot;Montanha Velha&quot; é o nome contemporâneo  para uma antiga cidade andina construído em meados do século XV, no promontório  rochoso que liga as montanhas de <strong>Machu Picchu</strong> e <strong>Huayna Picchu</strong> nas encostas  orientais das montanhas centrais ocorre sul Peru<br>
  <br>
  <strong>Machu  Picchu</strong> está localizada a 2.490 m, altitude da praça principal, onde seu nome  original era Picchu ou Piccho finalmente chamado pelas montanhas que cercana a  cidade de <strong>Machu Picchu</strong>.<br>
  <br>
  De acordo  com documentos a partir de meados do século XVI, <strong>Machu Picchu</strong> teria sido uma  das residências resto do <strong>Pachacutec</strong>, o nono Inca do<strong> imperio Tahuantinsuyo</strong>, entre  1438 e 1470. No entanto, alguns dos seus melhores edifícios e cerimonial  evidente a partir da porta de entrada principal para o llaqta ou <strong>Machu Picchu</strong>,  demonstrar que esta foi usada como um santuário religioso.<br>
  <br>
  <strong>Machu  Picchu</strong> é considerado tanto uma obra-prima da arquitetura e ingeneiria. As suas  características arquitectónicas únicas e paisagismo, tornaram-se <strong>Machu Picchu</strong> um dos destinos turísticos mais populares do planeta.<br>
  <br>
  <strong>Machu  Picchu</strong> está na Lista do Património Mundial da <strong>UNESCO</strong> desde 1983 como parte de  um grupo cultural e ecológico conhecido sob o nome de <strong>Santuário Histórico de  Machu Picchu</strong> A 07 julho de 2007 <strong>Machu Picchu</strong> foi declarado um dos <strong>sete  Maravilhas do Mundo</strong> Moderno em uma cerimônia em <strong>Lisboa</strong>, <strong>Portugal</strong>, que teve a  participação de uma centena de milhões de eleitores em todo o mundo, a escolha  de <strong>Machu Picchu</strong> como um deles.</p>
